public class Buku
{
    private String judul;
    private String penulis;
    private String penerbit;
    private int jumlahTerjual;
    
    // Tuliskan sebuah constructor yang menerima sebuah argument
    // untuk setiap field di bawah!
    
      public Buku(String judul, String penulis, String penerbit, int jumlahTerjual) {

        this.judul = judul;

        this.penulis = penulis;

        this.penerbit = penerbit;

        this.jumlahTerjual = jumlahTerjual;

    }


    public String getJudul() {

        return judul;

    }

 

    public void setJudul(String judul) {

        this.judul = judul;

    }

 

    public String getPenulis() {

        return penulis;

    }

 

    public void setPenulis(String penulis) {

        this.penulis = penulis;

    }

 

    public String getPenerbit() {

        return penerbit;

    }

 

    public void setPenerbit(String penerbit) {

        this.penerbit = penerbit;

    }

 

    public int getJumlahTerjual() {

        return jumlahTerjual;

    }

 

    public void setJumlahTerjual(int jumlahTerjual) {

        this.jumlahTerjual = jumlahTerjual;

    }
}

